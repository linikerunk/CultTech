package classes;

public class Musica {
	
	private String album;
	private String tempo_album;
	private String cantor;
	private int ano;
	private String genero;
	private int qtd_faixas;
	public String getAlbum() {
		return album;
	}
	public void setAlbum(String album) {
		this.album = album;
	}
	public String getTempo_album() {
		return tempo_album;
	}
	public void setTempo_album(String tempo_album) {
		this.tempo_album = tempo_album;
	}
	public String getCantor() {
		return cantor;
	}
	public void setCantor(String cantor) {
		this.cantor = cantor;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public String getGenero() {
		return genero;
	}
	public void setGenero(String genero) {
		this.genero = genero;
	}
	public int getQtd_faixas() {
		return qtd_faixas;
	}
	public void setQtd_faixas(int qtd_faixas) {
		this.qtd_faixas = qtd_faixas;
	}
	
	

}